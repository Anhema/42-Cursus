#include <stddef.h>

void	ft_bzero(void *s, size_t n)
{
	int				i;
	unsigned char	*temp;

	if (n > 0)
	{
		i = 0;
		temp = s;
		while (n > 0)
		{
			temp[i] = '\0';
			i++;
			n--;
		}
	}
}
